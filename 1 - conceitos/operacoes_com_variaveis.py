# Operacoes numericas
# 1 - valores inteiros
print('valores inteiros')
x = 100
y = 30

print('Soma: x + y')
print(x + y)

print('Subtracao: x - y')
print(x - y)

print('Multiplicacao: x * y')
print(x * y)

print('Divisao: x / y')
print(x / y)

print('Resto da divisao: x/y')
print(x % y)


